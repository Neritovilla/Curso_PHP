<?php

//operador pre-incremento +=
$numero = 10;
$numero = ++$numero;
echo "<br>numero: " . $numero;

//operador post-incremento ++
$numero = 10;
$numero = $numero++;
echo "<br>numero: " . $numero;

//operador pre-decremento -=
$numero = 20;
$numero = --$numero;
echo "<br>numero: " . $numero;

//operador post-decremento --
$numero = 20;
$numero = $numero--;
echo "<br>numero: " . $numero;



?>
